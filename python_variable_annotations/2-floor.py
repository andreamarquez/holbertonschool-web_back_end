#!/usr/bin/env python3
"""
This module provides a function to calculate the floor of a float.
"""

import math

def floor(n: float) -> int:
    """
    Return the floor of a floating-point number.

    Args:
        n (float): The number to calculate the floor for.

    Returns:
        int: The floor of the number.
    """
    return math.floor(n)
    